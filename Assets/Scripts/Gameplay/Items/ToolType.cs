
public enum ToolType {

	None = 0,

	/// <summary> Smashes things into pieces. Useful for
	///           early-game crafting and resource gathering. </summary>
	Hammer,

	/// <summary> Chops plants and trees down and into smaller pieces. Useful
	///           for early- to mid-game resource gathering and some crafting. </summary>
	Axe,

	/// <summary> Breaks apart harder materials for gathering raw stone,
	///           ores and other minerals to progress in mid- to late-game. </summary>
	Pickaxe,

	/// <summary> Slices or seperates things into parts,
	///           like meat or raw hide from animals. </summary>
	Blade

}

