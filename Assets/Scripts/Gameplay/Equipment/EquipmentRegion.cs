
/// <summary> Regions describe the location of an equipment slot on
///           an entity. Together with equipment tags, they're used
///           to restrict where items can be equipped. </summary>
public enum EquipmentRegion {

	None = 0,

	Head,
	Chest,
	Back,

	ArmsUpper,
	ArmsLower,
	Hands,

	LegsUpper,
	LegsLower,
	Feet 

}
